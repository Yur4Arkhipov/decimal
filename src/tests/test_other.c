Suite *suite_truncate(void) {
  Suite *s = suite_create("s21_truncate");
  TCase *tc = tcase_create("case_truncate");

  tcase_add_loop_test(tc, truncate_test, 0, LEN(case_dataset_truncate));

  suite_add_tcase(s, tc);

  return s;
}

void test_truncate(s21_decimal decimal, s21_decimal decimal_check) {
  s21_decimal result;

  int code = s21_truncate(decimal, &result);
  //   int sign_check = decimal_check.sign;
  int sign_check = s21_util_get_sign(&decimal_check);
  //   int sign_result = result.sign;
  int sign_result = s21_util_get_sign(&result);

  ck_assert_int_eq(code, OK);
  ck_assert_int_eq(s21_is_equal(result, decimal_check), 1);
  ck_assert_int_eq(sign_check, sign_result);
}

